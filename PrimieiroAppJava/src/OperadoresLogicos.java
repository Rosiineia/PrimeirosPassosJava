public class OperadoresLogicos {
    public static void main(String[] args){

        /* operadores logicos: && = and; || = OR */
        int idade = 18;
        float salario= 1000f;

        System.out.println(idade >= 18 && salario >= 3000);
        System.out.println(idade >= 18 || salario >= 3000);
    }
}
